using System.Text;
using AvaloniaDesigner.Generator.Models;
using AvaloniaDesigner.Generator.Services;
using Microsoft.CodeAnalysis;

namespace AvaloniaDesigner.Generator.Builders
{
    public class FormClassBuilder
    {
        private readonly TypeResolver _resolver;
        private readonly ValueFormatter _formatter;
        private readonly SourceProductionContext _context; // Для репортинга ошибок

        public FormClassBuilder(TypeResolver resolver, SourceProductionContext context)
        {
            _resolver = resolver;
            _context = context;
            _formatter = new ValueFormatter(resolver);
        }

        public string Build(FormModel model, string rootNamespace)
        {
            var sb = new StringBuilder();
            string ns = $"{rootNamespace}.{model.NamespaceSuffix}";

            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("using Avalonia.Controls;");
            sb.AppendLine("using Avalonia.Layout;");
            sb.AppendLine("using Avalonia.Media;");
            sb.AppendLine();
            sb.AppendLine($"namespace {ns}");
            sb.AppendLine("{");
            sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCode(\"AvaloniaDesigner.Generator\", \"1.0.0.0\")]");
            sb.AppendLine($"    public partial class {model.FormName} : global::{model.ParentClassType}");
            sb.AppendLine("    {");

            // Поля
            foreach (var control in model.Controls)
            {
                sb.AppendLine($"        internal global::{control.Type} {control.Name};");
            }

            sb.AppendLine();
            sb.AppendLine("        public void InitializeComponent()");
            sb.AppendLine("        {");

            BuildMethodBody(sb, model);

            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }

        private void BuildMethodBody(StringBuilder sb, FormModel model)
        {
            // 1. Root Container
            string rootVar = "root";
            sb.AppendLine($"            global::{model.RootContainer.Type} {rootVar} = new global::{model.RootContainer.Type}();");
            
            var rootType = _resolver.ResolveType(model.RootContainer.Type);
            
            // Свойства Root
            foreach (var prop in model.RootContainer.Properties)
            {
                ApplyProperty(sb, rootVar, rootType, prop.Key, prop.Value);
            }

            // 2. Child Controls
            // Определяем, как добавлять детей в Root (Add vs Content =)
            var (rootContentPropName, rootUseAdd) = GetContentStrategy(rootType);

            foreach (var control in model.Controls)
            {
                sb.AppendLine($"            this.{control.Name} = new global::{control.Type}();");
                sb.AppendLine($"            this.{control.Name}.Name = \"{control.Name}\";");

                var controlType = _resolver.ResolveType(control.Type);

                foreach (var prop in control.Properties)
                {
                    ApplyProperty(sb, $"this.{control.Name}", controlType, prop.Key, prop.Value);
                }

                // Добавление в Root
                if (rootUseAdd)
                    sb.AppendLine($"            {rootVar}.{rootContentPropName}.Add(this.{control.Name});");
                else
                    sb.AppendLine($"            {rootVar}.{rootContentPropName} = this.{control.Name};");
            }

            // 3. Assign Root to Parent (this)
            var parentType = _resolver.ResolveType(model.ParentClassType);
            var (parentContentProp, parentUseAdd) = GetContentStrategy(parentType);

            if (parentUseAdd)
                sb.AppendLine($"            this.{parentContentProp}.Add({rootVar});");
            else
                sb.AppendLine($"            this.{parentContentProp} = {rootVar};");
        }

        private void ApplyProperty(StringBuilder sb, string targetName, INamedTypeSymbol? typeSymbol, string key, System.Text.Json.JsonElement value)
        {
            // Attached Property
            if (key.Contains("."))
            {
                HandleAttachedProperty(sb, targetName, key, value);
                return;
            }

            // Regular Property
            var propSymbol = typeSymbol != null ? _resolver.FindProperty(typeSymbol, key) : null;
            string valueExpr = _formatter.Format(value, propSymbol?.Type);
            sb.AppendLine($"            {targetName}.{key} = {valueExpr};");
        }

        private void HandleAttachedProperty(StringBuilder sb, string targetName, string key, System.Text.Json.JsonElement value)
        {
            int lastDot = key.LastIndexOf('.');
            string ownerName = key.Substring(0, lastDot);
            string propName = key.Substring(lastDot + 1);

            var ownerType = _resolver.ResolveType(ownerName);
            var setter = ownerType != null ? _resolver.FindAttachedSetter(ownerType, propName) : null;

            if (ownerType != null && setter != null)
            {
                var valType = setter.Parameters[1].Type;
                string valueExpr = _formatter.Format(value, valType);
                sb.AppendLine($"            global::{ownerName}.{setter.Name}({targetName}, {valueExpr});");
            }
            else
            {
                // Диагностика (Warning)
                _context.ReportDiagnostic(Diagnostic.Create(
                    new DiagnosticDescriptor("ADG0002", "Attached Property Error", 
                    $"Setter not found for {key}", "Generation", DiagnosticSeverity.Warning, true), Location.None));
            }
        }

        private (string PropName, bool UseAdd) GetContentStrategy(INamedTypeSymbol? typeSymbol)
        {
            if (typeSymbol == null) return ("Content", false);

            var contentProp = _resolver.FindContentProperty(typeSymbol);
            string name = contentProp?.Name ?? "Content"; // Default fallback if no attribute

            if (contentProp != null)
            {
                bool noSetter = contentProp.SetMethod == null;
                bool isCollection = _resolver.IsCollectionType(contentProp.Type);
                return (name, noSetter && isCollection);
            }
            
            // Hardcoded fallback logic check based on name if attribute missing
            if (name == "Children" || name == "Items") return (name, true);
            
            return (name, false);
        }
    }
}